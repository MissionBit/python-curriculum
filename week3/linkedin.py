#Example of LISTS


#You want to set up a linkedin account to have a record of all of your experiences and education


#You set up your account and list these items as your experiences and education when it asks you for information
my_experiences = ["Waiter", "Coder"]
my_education = ["High School"]

#You go to college and want to update your Linkedin!

#Luckily you can go back and add to your list of experiences and education
my_experiences.append("Teaching assistant")
my_education.append("College")

#Now you will get Waiter, Coder AND Teaching Assistant
print(my_experiences)

#Now you will get High School AND College
print(my_education)
